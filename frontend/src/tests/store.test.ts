import { describe, it, expect, beforeEach } from "vitest";
import { useChurnStore } from "../store/useChurnStore";

describe("useChurnStore", () => {
  beforeEach(() => {
    useChurnStore.setState({ prediction: null, retention: null });
  });

  const mockPrediction = {
    prediction: 1,
    churn_probability: 0.85,
    retention_risk: "High",
  };

  const mockRetention = {
    script: "[Generated by Llama-3] We value you as a customer.",
  };

  it("starts with null prediction and retention", () => {
    const state = useChurnStore.getState();
    expect(state.prediction).toBeNull();
    expect(state.retention).toBeNull();
  });

  it("setResults stores both prediction and retention", () => {
    useChurnStore.getState().setResults(mockPrediction, mockRetention);
    const state = useChurnStore.getState();
    expect(state.prediction).toEqual(mockPrediction);
    expect(state.retention).toEqual(mockRetention);
  });

  it("setResults overwrites previous values", () => {
    useChurnStore.getState().setResults(mockPrediction, mockRetention);

    const updatedPrediction = {
      prediction: 0,
      churn_probability: 0.25,
      retention_risk: "Low",
    };
    const updatedRetention = {
      script: "[Fallback Script] Default script.",
    };

    useChurnStore.getState().setResults(updatedPrediction, updatedRetention);
    const state = useChurnStore.getState();
    expect(state.prediction?.retention_risk).toBe("Low");
    expect(state.retention?.script).toContain("[Fallback Script]");
  });

  it("clearResults resets both to null", () => {
    useChurnStore.getState().setResults(mockPrediction, mockRetention);
    useChurnStore.getState().clearResults();
    const state = useChurnStore.getState();
    expect(state.prediction).toBeUndefined();
    expect(state.retention).toBeUndefined();
  });

  it("clearResults is idempotent when already null", () => {
    useChurnStore.getState().clearResults();
    const state = useChurnStore.getState();
    expect(state.prediction).toBeUndefined();
    expect(state.retention).toBeUndefined();
  });

  it("preserves prediction when retention is updated", () => {
    useChurnStore.getState().setResults(mockPrediction, mockRetention);

    const newRetention = { script: "New script." };
    useChurnStore.getState().setResults(mockPrediction, newRetention);
    const state = useChurnStore.getState();
    expect(state.prediction?.prediction).toBe(1);
    expect(state.retention?.script).toBe("New script.");
  });
});
